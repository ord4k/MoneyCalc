package com.example.model;


public class People {
	private String name;
	private int cash;
	
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return this.name;
		
	}
	
	public void setCash(int cash) {
		this.cash = cash;
	}
	public int getCash() {
		return this.cash;
	}

}
